package increament_decrement_operator;

public class Increment {
	public static void main(String[] args) {
		
	
	int a = 10;
	a++;
	System.out.println(a);
//	this is a post increment operator....
	
//	internally here internally a = (int) a + 1;
	
	int b = 20;
	++b;
	System.out.println(b);
//	this is anpost increment operator.......
	
//	internally here internally a = (int) a + 1;
	
	int c = 30;
	c--;
	System.out.println(c);
//	this is a post decrement operator.....
	
//	internally here internally a = (int) a - 1;
	
	
	int d = 40;
	--d;
	System.out.println(d);
//	this a predecrement operator.....
	
	}

}
